# Hier start de for-loop

my_list = []

# Gebruik een for-loop en range() om de lijst te vullen
for i in range(1, 6):  # range(1, 6) genereert getallen van 1 tot en met 5
    my_list.append(i * 10)  # Voeg het 10-voudige van i toe aan de lijst

# Print de lijst op het scherm
print(my_list)